export const MetricCard: React.FC<MetricCardProps> = ({
  title,
  value,
  change,
  changeType,
  icon,
}) => {
  const { t } = useLocalization();

  const getChangeColor = () => {
    switch (changeType) {
      case 'increase':
        return 'text-green-600 dark:text-green-400';
      case 'decrease':
        return 'text-red-600 dark:text-red-400';
      default:
        return 'text-gray-600 dark:text-gray-400';
    }
  };

  const getChangeIcon = () => {
    if (changeType === 'neutral') return null;
    
    return changeType === 'increase' ? (
      <svg className="h-4 w-4" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
        <path strokeLinecap="round" strokeLinejoin="round" d="M2.25 18L9 11.25l4.306 4.307a11.95 11.95 0 015.814-5.519l2.74-1.22m0 0l-5.94-2.28m5.94 2.28l-2.28 5.941" />
      </svg>
    ) : (
      <svg className="h-4 w-4" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
        <path strokeLinecap="round" strokeLinejoin="round" d="M2.25 6L9 12.75l4.286-4.286a11.948 11.948 0 014.306 6.43l.776 2.898m0 0l3.182-5.511m-3.182 5.511l-5.511-3.182" />
      </svg>
    );
  };

  return (
    <div className="bg-white dark:bg-gray-800 overflow-hidden shadow rounded-lg">
      <div className="p-5">
        <div className="flex items-center">
          <div className="flex-shrink-0">
            <div className="p-3 bg-blue-50 dark:bg-blue-900/50 rounded-md">
              <div className="text-blue-600 dark:text-blue-400">
                {icons[icon]}
              </div>
            </div>
          </div>
          <div className="ml-5 rtl:ml-0 rtl:mr-5 w-0 flex-1">
            <dl>
              <dt className="text-sm font-medium text-gray-500 dark:text-gray-400 truncate">
                {title}
              </dt>
              <dd className="flex items-baseline">
                <div className="text-2xl font-semibold text-gray-900 dark:text-white">
                  {value}
                </div>
                {changeType !== 'neutral' && (
                  <div className={cn(
                    'ml-2 rtl:ml-0 rtl:mr-2 flex items-baseline text-sm font-semibold',
                    getChangeColor()
                  )}>
                    {getChangeIcon()}
                    <span className="ml-1 rtl:ml-0 rtl:mr-1">
                      {Math.abs(change).toFixed(1)}%
                    </span>
                  </div>
                )}
              </dd>
            </dl>
          </div>
        </div>
      </div>
      {changeType !== 'neutral' && (
        <div className="bg-gray-50 dark:bg-gray-700 px-5 py-3">
          <div className="text-sm">
            <span className="text-gray-600 dark:text-gray-400">
              {changeType === 'increase' 
                ? t('admin.dashboard.increaseFromLastMonth', 'Increase from last month')
                : t('admin.dashboard.decreaseFromLastMonth', 'Decrease from last month')
              }
            </span>
          </div>
        </div>
      )}
    </div>
  );
};

export default MetricCard;